// Function to calculate total working hours per month for each user on a project
function calculateTotalHoursPerMonth(tasks) {
  const hoursPerMonth = {};

  // Loop through each task
  tasks.forEach((task) => {
    const userId = task.userId;
    const projectId = task.projectId;
    const startDate = new Date(task.startTime);
    const endDate = new Date(task.endTime);
 
 
    const firstDayOfYear = new Date(new Date().getFullYear(), 0, 1);
    const lastDayOfYear = new Date(new Date().getFullYear(), 11, 31);

    // var startMonth = new Date(new Date().getFullYear(), 0, 1);
    // var endMonth = new Date(new Date().getFullYear(), 11, 31);
    
    const startMonth = firstDayOfYear.getMonth(); // Month index (0-11)
    const endMonth = lastDayOfYear.getMonth(); // Month index (0-11)

    // Iterate over months in the task duration
    for (let month = startMonth; month <= endMonth; month++) {
      const key = `${projectId}_${userId}_${month}`; // Unique key for each month and user
      const daysInMonth = new Date(
        startDate.getFullYear(),
        month + 1,
        0
      ).getDate(); // Total days in the month
      const hoursWorked = (endDate - startDate); // Convert milliseconds to hours
      const hoursPerDay = hoursWorked / daysInMonth; // Average hours worked per day
      if (!hoursPerMonth[key]) {
        hoursPerMonth[key] = {
          projectId: projectId,
          userId: userId,
          month: month,
          totalHours: 0,
        };
      }
      hoursPerMonth[key].totalHours += hoursPerDay;
    }
  });

  return hoursPerMonth;
}

