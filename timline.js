  function calculateWorkingHours(startTime, endTime) {
    const start = new Date(startTime);
    const end = new Date(endTime);
    const diff = (end - start) / (1000 * 60 * 60); // Convert milliseconds to hours
    return diff;
  }
  
  // Function to calculate working hours for each member from January to December
  function calculateWorkHoursPerMonth(member) {
    const workingHoursPerMonth = Array(12).fill(0); // Initialize an array to store working hours for each month
  
    member.timelines.forEach(timeline => {
      const startDate = new Date(timeline.startTime);
      const endDate = new Date(timeline.endTime);
  
      const startMonth = startDate.getMonth();
      const endMonth = endDate.getMonth();
      const startYear = startDate.getFullYear();
      const endYear = endDate.getFullYear();
  
      if (startYear === endYear) {
        if (startYear === 2024) { // Assuming the year is 2024
          for (let month = startMonth; month <= endMonth; month++) {
            const hours = calculateWorkingHours(startDate, endDate);
            workingHoursPerMonth[month] += hours;
          }
        }
      }
      // You can add handling for cases where timelines span across years if needed
    });
  
    return workingHoursPerMonth;
  }
  
